/*
**************** Version 2.0 ****************
Program:
*	Fills 2 DMA buffers (adcValue,adcValue2) with 4096 samples
*	Once buffers are half/full with new values, a respective flag is raised
*	Main loop checks for the spoken flags, converts ADC values to -1 < floats < 1, and Cross-Corelates between the 2 channels
[***PLEASE NOTE*** When float arrays are filled, they no longer correspond to ADC arrays, as ADC arrays are already filled with new content]
*	Main loop finds AOA of the source that was sampled through the analog GPIOs
*	The AOA is then sent as a message ("sxx") through UART which is received by the drone.
*	ADC sampling frequency is calculated by the relevant formula from documentation

	
**************** End of Notes ***************
*/

