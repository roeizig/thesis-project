int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	arm_fill_f32(0.0f, (float32_t*) &cor, 2 * shift + 1U);
	arm_fill_f32(0.0f, (float32_t*) &floatCh1, capturedPoints);
	arm_fill_f32(0.0f, (float32_t*) &floatCh2, capturedPoints2);
	arm_fill_f32(0.0f, (float32_t*) &angle, ANGLE_LEN);
	__HAL_ADC_CLEAR_FLAG(&hadc1, ADC_FLAG_AWD);
	AWDEnabled = __HAL_ADC_GET_IT_SOURCE(&hadc1, ADC_IT_AWD);
	
	
	uartTx[3] = '\n';
	uartTx[4] = '\r';
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_ADC1_Init();
  MX_TIM5_Init();
  MX_ADC2_Init();

  /* USER CODE BEGIN 2 */
	
	
	/* Roei: Starting in DMA mode, EOC will be triggered only after AWDFlag is on */
	HAL_ADC_Start_DMA(&hadc2, (uint32_t*) adcValue2, capturedPoints2);
	HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t*) adcValue, capturedPoints);


	HAL_TIM_PWM_Start(&htim5,TIM_CHANNEL_ALL); 

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		/* Roei: Added for stopping the CF2 when userButton is pressed
		*
		*/
		if (CFStopFlag == 1U) {
			uartTx[2] = 48U;
			uartTx[1] = 48U;
			uartTx[0] = 50U;
			/* Send the CF2 '200' */
			HAL_UART_Transmit(&huart2, uartTx, sizeof(uartTx), 1000);
			CFStopFlag = 0U;
		}
		// Roei: End of additions
		if (halfCpltFlag == 1U) {
			ADCToFloat(&adcValue[0], floatCh1, capturedPoints / 2);
			ADCToFloat(&adcValue2[0], floatCh2, capturedPoints2 / 2);
			halfCpltFlag = 0U;
		}
		if (cpltFlag == 1U) {
			__disable_irq();
			// Stop DMA ADC to Buffer conversion
			HAL_ADC_Stop_DMA(&hadc2);
			HAL_ADCEx_MultiModeStop_DMA(&hadc1);
			// Convert ADC Values for Analysis
			ADCToFloat(&adcValue[capturedPoints / 2], &floatCh1[capturedPoints / 2], capturedPoints / 2);
			ADCToFloat(&adcValue2[capturedPoints2 / 2], &floatCh2[capturedPoints2 / 2], capturedPoints2 / 2);
			// Cross Correlate and Find Source
			partialXCorr(floatCh1, &cpltCntr, floatCh2, cor);
			angle[0] = AOADetection(cor, &maxShift);
			/*		Conversion of angle[0] to chars for UART Tx 		*/
			uartTx[2] = lroundf(fabsf(angle[0])) % 10 + 48U;
			uartTx[1] = lroundf(fabsf(angle[0])) / 10 + 48U;
			uartTx[0] = signbit(angle[0]) + 48U;
			HAL_UART_Transmit(&huart2, uartTx, sizeof(uartTx), 1000);
			
			// Reset the Complete Flag
			cpltFlag = 0U;
			// Reset the AWD Flag
			AWDFlag = 0U;
			
			/* Re-enable ADC in DMA mode */
			HAL_ADC_Start_DMA(&hadc2, (uint32_t*) adcValue2, capturedPoints2);
			HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t*) adcValue, capturedPoints);
			
			__enable_irq();
		}

  }
  /* USER CODE END 3 */

}

